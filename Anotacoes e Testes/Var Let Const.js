// Var === variante
var clima = "Quente"
clima = "Frio" // alterando a variavel
console.log(clima)
//retorna frio

// let
let clima1 = "Quente"
clima1 = "Frio" // alterando a variavel
console.log(clima1)
//retorna frio

// const === constante
const clima2 = "Quente"
clima2 = "Frio" // alterando a variavel
console.log(clima2)
//retorna Uncaught TypeError: Assignment to constant variable.